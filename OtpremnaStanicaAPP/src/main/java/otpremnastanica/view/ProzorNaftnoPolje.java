    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
         obrada.setEntitet(new NaftnoPolje());
        napuniModel();
        try {
            obrada.create();
            ucitaj();
        } catch (OtpremnaStanicaException ex) {
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
    }                                        
    
    private void napuniModel(){
        var np = obrada.getEntitet();
        np.setNaziv(txtNaziv.getText());
        
    
    }//GEN-LAST:event_btnDodajActionPerformed

    private void lstPodaciValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPodaciValueChanged
        if(evt.getValueIsAdjusting()){
            return;
        }
        if(lstPodaci.getSelectedValue()==null){
            return;
        }
        
        obrada.setEntitet(lstPodaci.getSelectedValue());
        
        napuniView();
    }//GEN-LAST:event_lstPodaciValueChanged

    private void btnPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjeniActionPerformed
      if(lstPodaci.getSelectedValue()==null){
          JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite naftno polje");
          
          return;
      }
      napuniModel();
        try {
            obrada.update();
            ucitaj();
        } catch (OtpremnaStanicaException ex) {
        JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnPromjeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(lstPodaci.getSelectedValue()==null){
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite naftno polje");
            
            return;
        }
        
        if(JOptionPane.showConfirmDialog(getRootPane(), "Sigurno obrisati "
        + obrada.getEntitet().getNaziv() + "?", "Brisanje", JOptionPane.YES_NO_OPTION,
        JOptionPane.QUESTION_MESSAGE)== JOptionPane.NO_OPTION){
            return;
        }
        
        try {
            obrada.delete();
            ucitaj();
        } catch (OtpremnaStanicaException ex) {
        JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
        
    }//GEN-LAST:event_btnObrisiActionPerformed

  private void napuniView(){
      var e = obrada.getEntitet();
      txtNaziv.setText(e.getNaziv());
      
      DefaultListModel<Busotina> m = new DefaultListModel<>();
     
      if(e.getBusotine()!=null && !e.getBusotine().isEmpty()){
        
          m.addAll(e.getBusotine());
      }
  
      
      lstBusotine.setModel(m);
      lstBusotine.repaint();
      
     btnObrisi.setVisible(false);
     if(e.getBusotine()==null || e.getBusotine().isEmpty()){
         btnObrisi.setVisible(true);
         
     }
  }
  

